    private boolean CreateAccValidation() {
        String error = "";
        //Name
        if (Validator.checkEmpty(txt_CreateAcc_Name)) {
            error += "Name cannot be null!\n";
        }
        //Address
        if (Validator.checkEmptyArea(txt_CreateAcc_Address)) {
            error += "Address cannot be null!\n";
        }
        //Contact
        if (Validator.checkEmpty(txt_CreateAcc_Contact)) {
            error += "Contact cannot be null!\n";
        }
        if (Validator.checkPhone(txt_CreateAcc_Contact)) {
            error += "Contact must be 10 numbers!\n";
        }
        //Birth
        if (Validator.checkDate(txt_CreateAcc_Birth)) {
            error += "Birth must be in dd-MM-yyyy!\n";
        }
        //Email
        if (Validator.checkEmpty(txt_CreateAcc_Email)) {
            error += "Email cannot be null!\n";
        }
        if (Validator.checkEmail(txt_CreateAcc_Email)) {
            error += "Wrong email format!\n";
        }
        //Submit
        if (!error.equals("")) {
            JOptionPane.showMessageDialog(null, error);
            return false;
        } else {
            return true;
        }
    }

